/**
 * @param {string[][]} grid
 * @return {number}
 */
const numIslands = function (grid) {
  let result = 0;
  for (let i = 0; i < grid.length; i++) {
    for (let j = 0; j < grid[i].length; j++) {
      if (grid[i][j] === '1') {
        result++
        const stack = [[i, j]]
        while (stack.length) {
          const [i, j] = stack.pop()
          grid[i][j] = ''
          if (grid[i][j + 1] === '1') {
            stack.push([i, j + 1])
          }
          if (grid[i][j - 1] === '1') {
            stack.push([i, j - 1])
          }
          if ((grid[i + 1] || [])[j] === '1') {
            stack.push([i + 1, j])
          }
          if ((grid[i - 1] || [])[j] === '1') {
            stack.push([i - 1, j])
          }
        }
      }
    }
  }
  return result
};

